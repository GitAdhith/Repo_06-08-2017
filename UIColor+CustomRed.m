#import "UIColor+CustomRed.h"

@implementation UIColor (CustomRed)

+ (UIColor *)CutomRedColor
{
    return [UIColor colorWithRed:20 green:20 blue:20 alpha:1];
}

+ (UIColor *)CutomBlueColor
{
    return [UIColor colorWithRed:30 green:30 blue:30 alpha:1];
}

+ (UIColor *)CutomGreenColor
{
    return [UIColor colorWithRed:40 green:40 blue:40 alpha:1];
}

@end
